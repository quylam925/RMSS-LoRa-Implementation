/**
* @brief This function handles USART1 global interrupt.
*/
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	uint32_t tmp_flag = 0, tmp_it_source = 0;

	/* 수신 이벤트 조사 */
	tmp_flag = __HAL_UART_GET_FLAG(&huart1, UART_FLAG_RXNE);
	tmp_it_source = __HAL_UART_GET_IT_SOURCE(&huart1, UART_IT_RXNE);
	/* UART in mode Receiver ---------------------------------------------------*/
	if ((tmp_flag != RESET) && (tmp_it_source != RESET)) {
	/* 수신 레지스터에서 링버퍼로 한 바이트 복사 */

		U1_RxBuffer[u1_rx_cnt] = huart1.Instance->DR & (uint8_t)0x00FF;

		// Check SOF/EOF
		if (U1_RxBuffer[u1_rx_cnt] == 0xDE)
			u1_rx_uwb_sof = 1;
		if (U1_RxBuffer[u1_rx_cnt] == 0xCA) {
			u1_rx_uwb_eof = 1;
			// Check Length error..
			if (u1_rx_cnt < 2) {
				u1_rx_uwb_eof = 0;
			}
			else if((U1_RxBuffer[2]+2) > u1_rx_cnt){
				//0xCA is located in the middle of data
				u1_rx_uwb_eof = 0;
			}
			else if((U1_RxBuffer[2]+2) < u1_rx_cnt){
				//Length information is wrong..
				u1_rx_uwb_eof = 0;
				u1_rx_uwb_sof = 0;
				u1_rx_uwb_err = 1;
				u1_rx_uwb_err_cnt = u1_rx_cnt;
				u1_rx_cnt = 0;
			}
		}

		// Only after SOF, u1_rx_cnt can increase
		if (u1_rx_uwb_sof == 1)
			u1_rx_cnt++;
	}

	__HAL_UART_CLEAR_PEFLAG(&huart1); /* 이벤트 플래그 삭제 */

	HAL_UART_IRQHandler(&huart1);
	return; /* IRQ 핸들러 종료 */


  /* USER CODE END USART1_IRQn 0 */

  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

